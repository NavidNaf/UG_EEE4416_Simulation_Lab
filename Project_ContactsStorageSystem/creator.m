function save_Callback(hObject, eventdata, handles)
% hObject    handle to save (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
%cont=struct('name',{},'number',{},'email',{},'address',{});
cont(1).name='sakib';
cont(1).number='01521331943';
cont(1).email='mashursakib1920@gmail.com';
cont(1).address='S-518';
n=length(cont);
% new=input('creat new contact?y or n ','s');
new='y';
while strcmp(new,'y')
    n=n+1;
    cont(n).name=get(handles.name,'String');
    cont(n).number=get(handles.number,'String');
    cont(n).email=get(handles.email,'String');
    cont(n).address=get(handles.address,'String');
    new='n';
    for i=1:n
        set(handles.listbox2,'string',cont(i).name);
        guidata(hObject, handles);
    end
end


